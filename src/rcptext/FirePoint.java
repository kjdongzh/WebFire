package rcptext;

public class FirePoint {

	private int l_Lon;
	private int k_Lat;
	
	public FirePoint(int l_Lon, int k_Lat) {
		this.l_Lon = l_Lon;
		this.k_Lat = k_Lat;
	}
	
	public int getL_Lon() {
		return l_Lon;
	}
	public void setL_Lon(int l_Lon) {
		this.l_Lon = l_Lon;
	}
	public int getK_Lat() {
		return k_Lat;
	}
	public void setK_Lat(int k_Lat) {
		this.k_Lat = k_Lat;
	}
	
	
}
